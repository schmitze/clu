#!/usr/bin/env bash
# ============================================================
# Adapter: Claude Code
# ============================================================
# Translates clu context into Claude Code's native
# configuration format (CLAUDE.md + CLI flags).
#
# Claude Code reads CLAUDE.md from the working directory and
# uses it as persistent system instructions. This adapter:
#   1. Writes the assembled prompt to a staging CLAUDE.md
#   2. Launches claude from the project's repo path (or home)
#   3. Handles post-session summarization
# ============================================================

adapter_launch() {
    local staging_dir="/tmp/clu/${AGENT_PROJECT_NAME}"
    mkdir -p "$staging_dir"

    # ── Write assembled prompt as CLAUDE.md ───────────────────

    local claude_md="$staging_dir/CLAUDE.md"
    cat > "$claude_md" << 'HEADER'
<!-- ============================================================
     AUTO-GENERATED by clu launcher.
     Do NOT edit manually — changes will be overwritten.
     Edit the source files in ~/.clu/ instead.
     ============================================================ -->

HEADER
    echo "$AGENT_PROMPT" >> "$claude_md"

    # ── Add memory file paths as references ───────────────────
    # Tell Claude Code where the live memory files are so it can
    # read/write them directly during the session.

    cat >> "$claude_md" << EOF

---

## Memory File Locations (live, read/write)

These are the actual files on disk. When the memory protocol says
to write to a memory file, write to these paths:

**Project memory:**
EOF

    for mf in "$AGENT_PROJECT_DIR"/memory/*.md; do
        [[ -f "$mf" ]] || continue
        echo "- \`$mf\`" >> "$claude_md"
    done

    cat >> "$claude_md" << EOF

**User memory (shared):**
EOF

    for mf in "$AGENT_HOME"/shared/memory/*.md; do
        [[ -f "$mf" ]] || continue
        echo "- \`$mf\`" >> "$claude_md"
    done

    cat >> "$claude_md" << EOF

**Agent memory (shared):**
EOF

    for mf in "$AGENT_HOME"/shared/agent/*.md; do
        [[ -f "$mf" ]] || continue
        echo "- \`$mf\`" >> "$claude_md"
    done

    # ── Determine launch directory ────────────────────────────

    # Use AGENT_REPO_PATH (set by launcher), fall back to $HOME
    local launch_dir="${AGENT_REPO_PATH:-$HOME}"
    if [[ -n "$AGENT_REPO_PATH" && "$AGENT_REPO_PATH" != *"no repo"* ]]; then
        if [[ -d "$AGENT_REPO_PATH" ]]; then
            launch_dir="$AGENT_REPO_PATH"
        else
            echo "⚠ Repo path '$AGENT_REPO_PATH' not found. Launching from $HOME."
            launch_dir="$HOME"
        fi
    fi

    # ── Copy CLAUDE.md into launch directory ──────────────────
    # Claude Code reads CLAUDE.md from CWD. We stage it there,
    # run the session, then clean up.

    local existing_claude_md=""
    if [[ -f "$launch_dir/CLAUDE.md" ]]; then
        # Back up any existing CLAUDE.md
        existing_claude_md="$launch_dir/CLAUDE.md.clu-backup"
        cp "$launch_dir/CLAUDE.md" "$existing_claude_md"
        echo "📋 Backed up existing CLAUDE.md → CLAUDE.md.clu-backup"
    fi

    cp "$claude_md" "$launch_dir/CLAUDE.md"

    # ── Inject session recovery / digest ────────────────────
    # If the last session was interrupted (no daily log written),
    # inject a recovery block with the conversation tail.
    # Otherwise, inject a short digest of recent sessions.

    local clu_scripts
    clu_scripts="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"

    local recovery_script="$clu_scripts/session-recovery.py"
    local digest_script="$clu_scripts/session-digest.py"

    if [[ -x "$recovery_script" && -n "$launch_dir" && -n "$AGENT_PROJECT_DIR" ]]; then
        local recovery
        recovery=$(python3 "$recovery_script" "$launch_dir" \
            --project-dir "$AGENT_PROJECT_DIR" \
            --scan-all "$AGENT_HOME" 2>/dev/null)
        if [[ -n "$recovery" ]]; then
            cat >> "$launch_dir/CLAUDE.md" << EOF

---

$recovery

For the full transcript, run:
\`python3 $digest_script $launch_dir --last N --max-chars M\`
EOF
            echo "🔄 Injected session recovery context (last session was interrupted)"
        elif [[ -x "$digest_script" ]]; then
            # Clean exit — inject short digest instead
            local digest
            digest=$(python3 "$digest_script" "$launch_dir" --last 1 --max-chars 300 --format md 2>/dev/null)
            if [[ -n "$digest" ]]; then
                cat >> "$launch_dir/CLAUDE.md" << EOF

---

## Last Session Digest (auto-injected)

$digest
EOF
                echo "📜 Injected last session digest into CLAUDE.md"
            fi
        fi
    fi

    # ── Cleanup handler ─────────────────────────────────────
    # Ensures CLAUDE.md is restored/removed even on Ctrl+C or crash.
    # Uses global vars because trap can't capture locals from outer scope.

    _CLU_LAUNCH_DIR="$launch_dir"
    _CLU_BACKUP="$existing_claude_md"

    _clu_cleanup_claude_md() {
        if [[ -n "${_CLU_BACKUP:-}" && -f "$_CLU_BACKUP" ]]; then
            mv "$_CLU_BACKUP" "$_CLU_LAUNCH_DIR/CLAUDE.md"
            echo "📋 Restored original CLAUDE.md"
        else
            rm -f "$_CLU_LAUNCH_DIR/CLAUDE.md"
        fi
        trap - EXIT INT TERM
    }

    trap _clu_cleanup_claude_md EXIT INT TERM

    # ── Parse extra flags from config ─────────────────────────

    local extra_flags=""
    extra_flags=$(grep -A5 "claude-code:" "$AGENT_HOME/config.yaml" 2>/dev/null \
        | grep "extra_flags:" | head -1 \
        | sed 's/.*extra_flags:[[:space:]]*//' | sed 's/^"//' | sed 's/"$//')

    # ── Launch Claude Code ────────────────────────────────────

    echo "🤖 Launching Claude Code..."
    echo "   Working directory: $launch_dir"
    echo "   CLAUDE.md: injected ($(wc -l < "$claude_md") lines)"
    echo ""
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
    echo ""

    # Record session start time
    local session_start
    session_start=$(date +"%Y-%m-%d %H:%M")

    # Actually launch claude
    (
        cd "$launch_dir"
        claude --dangerously-skip-permissions $extra_flags
    )
    local exit_code=$?

    echo ""
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"

    # ── Cleanup ───────────────────────────────────────────────
    _clu_cleanup_claude_md

    # ── Switch detection ────────────────────────────────────
    # If the agent wrote a switch-target file during the session,
    # relaunch clu with the target project instead of returning
    # to the shell.

    local switch_file="/tmp/clu/switch-target"
    if [[ -f "$switch_file" ]]; then
        local switch_target
        switch_target=$(cat "$switch_file")
        rm -f "$switch_file"

        if [[ -n "$switch_target" ]]; then
            echo ""
            echo "🔄 Switching to project: $switch_target"
            echo ""
            exec "$AGENT_HOME/launcher" "$switch_target"
        fi
    fi

    # ── Post-session prompt ───────────────────────────────────

    local auto_summarize
    auto_summarize=$(grep "auto_summarize:" "$AGENT_HOME/config.yaml" 2>/dev/null \
        | head -1 | sed 's/.*auto_summarize:[[:space:]]*//')

    if [[ "$auto_summarize" == "true" ]]; then
        echo ""
        read -p "📝 Run post-session summarizer? (y/n) " -n 1 -r
        echo ""
        if [[ $REPLY =~ ^[Yy]$ ]]; then
            _adapter_quick_journal "$session_start"
        fi
    fi

    return $exit_code
}

# ── Summarizer (uses claude non-interactively) ────────────────

adapter_summarize() {
    local project_dir="${1:-$AGENT_PROJECT_DIR}"
    local agent_home="${2:-$AGENT_HOME}"
    _adapter_quick_journal "$(date +"%Y-%m-%d %H:%M")"
}

_adapter_quick_journal() {
    local session_start="${1:-unknown}"
    local journal_file="$AGENT_PROJECT_DIR/memory/journal.md"
    mkdir -p "$(dirname "$journal_file")"

    echo ""
    echo "Quick session summary (what did you work on?):"
    read -r summary

    if [[ -n "$summary" ]]; then
        local entry
        entry=$(cat << EOF

## Session – $session_start
**Project:** $AGENT_PROJECT_NAME
**Persona(s):** $AGENT_PERSONA
**Summary:** $summary
**Next steps:** [fill in]

EOF
        )
        echo "$entry" >> "$journal_file"
        echo "✅ Appended to journal.md"
    else
        echo "⏭ Skipped."
    fi
}
